    // Metodo para capturar produto selecionado da tabela
    public Produto capturaProduto() {
        String nomeTabela = "" + tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 1);
        String codigoTabela = "" + tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 0);
        String precoTabela = "" + tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 2);
        Produto produtoTabela = new Produto(nomeTabela, Float.parseFloat(precoTabela), Integer.parseInt(codigoTabela));
        return produtoTabela;
    }

    // Metodo para limpar campos de texto
    public void limparCampos() {
        tfNome.setText("");
        tfPreco.setText("");
        tfCodigo.setText("");
        tfPesquisa.setText("");
    }

    // Metodo para preencher tabela de produtos
    public void preencherTabela(ArrayList<Produto> produtos) {
        produto = new GerenciadorDeProdutos();
        ArrayList dados = new ArrayList();

        // Define o nome de cada coluna
        String[] Colunas = new String[]{"Código", "Nome", "Preço"};

        // Percorre a lista de produtos na comanda
        for (Produto produto : produtos) {
            dados.add(new Object[]{produto.getCodigo(), produto.getNome(), produto.getPreco()});
        }

        // Cria um modelo da tabela
        Tabela modelo = new Tabela(dados, Colunas);

        /*
        Define a tabela de acordo com o modelo passado
        Seta os paramêtros da tabela(tamanho das colunas)
        Não é permitido ao usuario modificar ordem das colunas
        As colunas se redimensionam para preencher o espaço reservado
        Apenas um item selecionado por vez
         */
        tabelaProdutos.setModel(modelo);
        tabelaProdutos.getColumnModel().getColumn(0).setPreferredWidth(60);
        tabelaProdutos.getColumnModel().getColumn(1).setPreferredWidth(300);
        tabelaProdutos.getColumnModel().getColumn(2).setPreferredWidth(83);
        tabelaProdutos.getTableHeader().setReorderingAllowed(false);
        tabelaProdutos.setAutoResizeMode(tabelaProdutos.AUTO_RESIZE_OFF);
        tabelaProdutos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }

