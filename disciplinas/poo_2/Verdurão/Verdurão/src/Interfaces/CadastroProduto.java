    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            String nome = tfNome.getText();
            float preco = Float.parseFloat(tfPreco.getText());
            int codigo = Integer.parseInt(tfCodigo.getText());
            // Verifica se o código digitado já está cadastrado
            Produto produtoAux = produto.pesquisar(tfCodigo.getText());
            if (produtoAux.getNome() == null) {
                produto.cadastrar(nome, preco, codigo);
                preencherTabela(produto.getProdutos());
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
                limparCampos();
            } else {
                JOptionPane.showMessageDialog(null, "Codigo de produto já cadastrado!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            limparCampos();
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar produto!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {
            Produto novo = new Produto(tfNome.getText(), Float.parseFloat(tfPreco.getText()), Integer.parseInt(tfCodigo.getText()));
            produto.editar(produtoAntigo, novo);
            JOptionPane.showMessageDialog(null, "Alteração realizada com sucesso!");
            preencherTabela(produto.getProdutos());
            limparCampos();
            btnSalvar.setEnabled(false);
            btnCadastrar.setEnabled(true);
            btnEditar.setEnabled(false);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar produto!");
            limparCampos();
            btnSalvar.setEnabled(false);
            btnCadastrar.setEnabled(true);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        produtoRemover = capturaProduto();
        int resultado = JOptionPane.showConfirmDialog(null, "Confirmar remoção do produto?", "REMOÇÃO DE PRODUTO", JOptionPane.YES_NO_OPTION);
        if (resultado == JOptionPane.YES_OPTION) {
            produto.remover(produtoRemover);
            preencherTabela(produto.getProdutos());
            limparCampos();
            btnRemover.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnCadastrar.setEnabled(true);
            btnEditar.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Produto removido com sucesso!");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        int tamanhoPesquisa = tfPesquisa.getText().length();
        ArrayList pesquisa = produto.pesquisar(tfPesquisa.getText(), tamanhoPesquisa);
        // Verifica se a lista retornada contem elementos
        if (!pesquisa.isEmpty()) {
            preencherTabela(pesquisa);
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum produto localizado!");
            preencherTabela(produto.getProdutos());
            limparCampos();
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

